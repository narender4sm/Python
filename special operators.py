# identity operators
a=10
b=10
print(a is b)
print(a is not b)

a1='abc'
b1='abc'
print(a1 is b1)
print(a1 is not b1)

l1=[10,20,30]
l2=[10,20,30]
print(l1==l2)
print(l1 is l2)
print(l1 is not l2)

# membership operators
as1='abcdef'
print('a' in as1)
print('a' not in as1)
print(10 in l1)
print(40 in l1)